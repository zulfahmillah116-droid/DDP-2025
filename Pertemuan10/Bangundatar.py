def jajarganjar(alas,tinggi):
    return alas * tinggi 

def persegi(sisi):
    hasil = sisi **2
    return hasil

def persegi_panjang(p,l):
    hasil = p * l
    return hasil 

def lingkaran(r):
    hasil = 22/7 * r**2
    return hasil

def segitiga(alas,tinggi):
    hasil = 1/2 * alas * tinggi
    return hasil 

    